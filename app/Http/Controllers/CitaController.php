        'categoria' => 'required',
        'estado' => 'required',
        'precio' => 'required|numeric',     
        'modalidad' => 'required',
        'fecha' => 'required|date_format:Y-m-d\TH:i'
        ]);
        $cita = Cita::create($datos);
        return redirect()->to('/adminCita');
    }

